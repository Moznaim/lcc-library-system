    private void populateJtablewithBooks() {
        ArrayList<com.libmngmsys.librarysystem.Books> bookList = books.bookList();
        
        //jtablecolumns
        String[] colNames = {"ID", "bookname", "Main Class", "Sub Class", "Location", "File Name"};
        
       DefaultTableModel model = new DefaultTableModel(colNames, 0); // Initialize model without data
        //row
     for (com.libmngmsys.librarysystem.Books book : bookList) {
        Object[] row = {
            book.getId(),
            book.getBookname(),
            book.getMainclass(),
            book.getSubclass(),
            book.getLocation(),
           book.getFilename()
        };
        model.addRow(row);
    }

    jTable_books.setModel(model);
    }
