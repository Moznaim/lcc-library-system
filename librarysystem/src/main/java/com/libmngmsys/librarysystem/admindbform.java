    private void jButton_choosefileMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton_choosefileMouseClicked
        // TODO add your handling code here:
        JFileChooser filechooser = new JFileChooser();
        filechooser.setDialogTitle("Set Photo");
 
        FileNameExtensionFilter filter = new FileNameExtensionFilter("Image Files", "jpg", "jpeg", "png");
        filechooser.setFileFilter(filter);
        
        int fileState = filechooser.showSaveDialog(null);
        
        if(fileState == JFileChooser.APPROVE_OPTION) {
            java.io.File selectedFile = filechooser.getSelectedFile();

            // Extract file name without path
            String path = selectedFile.getName();
            
            jLabel_path.setText(path);
        }
    }//GEN-LAST:event_jButton_choosefileMouseClicked

    private void jButton_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_addActionPerformed
        
        String bookname = jTextField_bookname.getText();
        String mainclass = jComboBox_mainclass.getSelectedItem().toString();
        String subclass = jTextField_subclass.getText();
        String location = jTextField_location.getText();
        String filename = jLabel_path.getText();
        
        if(bookname.isEmpty()) {
            jLabel_emptybookname.setVisible(true);
        } else if (mainclass == null || mainclass.toString().isEmpty()) {
            jLabel_emptymainclass.setVisible(true);
        } else if (subclass.isEmpty()) {
            jLabel_emptysubclass.setVisible(true);
        } else if (location.isEmpty()){
            jLabel_emptylocation.setVisible(true);
        } else if (filename == null || filename.isEmpty()) {
            System.out.println("Error");
        } else {
            books.addBook(bookname, mainclass, subclass, location, filename);
            populateJtablewithBooks();
        }
    }//GEN-LAST:event_jButton_addActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        //editbook
        String bookname = jTextField_bookname.getText();
        String mainclass = jComboBox_mainclass.getSelectedItem().toString();
        String subclass = jTextField_subclass.getText();
        String location = jTextField_location.getText();
        String filename = jLabel_path.getText();
        
        if(bookname.isEmpty()){
            System.out.println("Error");
        } else if (mainclass.isEmpty()){
            System.out.println("ERror");
        } else {
            try{
                int id = Integer.parseInt(jTextField_id.getText());
                books.editBook(id, bookname, mainclass, subclass, location, filename);
                populateJtablewithBooks();
            } catch(NumberFormatException ex) {
                JOptionPane.showMessageDialog(null, "Inavlid Book ID" + ex.getMessage());
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton_removeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_removeActionPerformed
        // TODO add your handling code here:
        try {
            int id = Integer.parseInt(jTextField_id.getText());
            books.removeBook(id);
            populateJtablewithBooks();
        } catch(NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Invalid Book ID" + ex.getMessage());
        }
    }//GEN-LAST:event_jButton_removeActionPerformed

    private void jTable_booksMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable_booksMouseClicked
        // TODO add your handling code here:
        int index = jTable_books.getSelectedRow();
        
        //get values
        String id = jTable_books.getValueAt(index, 0).toString();
        String bookname = jTable_books.getValueAt(index, 1).toString();
        String mainclass = jTable_books.getValueAt(index,2).toString();
        String subclass = jTable_books.getValueAt(index, 3).toString();
        String location = jTable_books.getValueAt(index, 4).toString();
        String filename = jTable_books.getValueAt(index, 5).toString();
        
        //show data in the info field
        jTextField_id.setText(id);
        jTextField_bookname.setText(bookname);
        jComboBox_mainclass.setSelectedItem(mainclass);
        jTextField_subclass.setText(subclass);
        jTextField_location.setText(location);
        jLabel_path.setText(filename);
    }//GEN-LAST:event_jTable_booksMouseClicked

